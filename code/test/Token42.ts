import { loadFixture } from "@nomicfoundation/hardhat-toolbox/network-helpers";
import { anyValue } from "@nomicfoundation/hardhat-chai-matchers/withArgs";
import { expect } from "chai";

const { ethers } = require("hardhat");

describe("Token42", function () {
  // We define a fixture to reuse the same setup in every test.
  // We use loadFixture to run this setup once, snapshot that state,
  // and reset Hardhat Network to that snapshot in every test.
  async function deployToken42Fixture() {
    const supply = 10 ** 7;

    // Contracts are deployed using the first signer/account by default
    const [owner, otherAccount] = await ethers.getSigners();

    const Token42 = await ethers.getContractFactory("Token42");
    const token42 = await Token42.deploy(supply);

    return { token42, supply, owner, otherAccount };
  }

  describe("Deployment", function () {
    it("Should set the right supply", async function () {
      const { token42, supply } = await loadFixture(deployToken42Fixture);

      expect(await token42.totalSupply()).to.equal(supply);
    });

    it("Should set the right owner", async function () {
      const { token42, owner } = await loadFixture(deployToken42Fixture);

      expect(await token42.creator()).to.equal(owner.address);
    });

    it("Should give the owner all the supply", async function () {
      const { token42, supply, owner } = await loadFixture(
        deployToken42Fixture
      );

      expect(await token42.balanceOf(owner.address)).to.equal(supply);
    });
  });
});

